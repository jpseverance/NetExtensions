using System;

namespace NetExtensions.Collections
{
    [Serializable]
    public class ObjectRemovedEventArgs : EventArgs
    {
        #region Event Handlers
        #endregion

        #region Methods
        #endregion

        #region Properties
        public object RemovedObject
        {
            get
            {
                return i_RemovedObject;
            }
            set
            {
                i_RemovedObject = value;
            }
        }
        #endregion

        #region Private Methods
        #endregion

        #region Private Properties
        #endregion

        #region Construction and Finalization
        public ObjectRemovedEventArgs( object removedObject )
        {
            this.RemovedObject = removedObject;
        }
        #endregion

        #region Data Elements
        private object i_RemovedObject;
        #endregion

        #region Constants
        #endregion
    }
}
