using System;
using System.Collections;

namespace NetExtensions.Collections
{
	/// <summary>
	/// A Bag is a collection that may contain duplicate elements;
	/// that is, the same instance may occur in a bag more than once.  
	/// Elements in a bag are not ordered.
	/// </summary>
	[Serializable]
	public class Bag : AbstractCollection
	{
		#region Event Handlers
		#endregion

		#region Methods
		#endregion

		#region Properties
		#endregion

		#region Private Methods
		#endregion

		#region Private Properties
		#endregion

		#region Construction and Finalization
		public Bag( ICollection c ) : base( c )
		{
		}

		public Bag( int capacity ) : base( capacity )
		{
		}

		public Bag() : base()
		{
		}
		#endregion

		#region Data Elements
		#endregion

		#region Constants
		#endregion
	}
}
